public class Calculator {

    void sum(int num1, int num2) {
        System.out.printf("%d + %d = %d\n", num1, num2, num1 + num2);
    }

    void multiply(int num1, int num2) {
        System.out.printf("%d * %d = %d\n", num1, num2, num1 * num2);
    }

    void subtract(int num1, int num2) {
        System.out.printf("%d - %d = %d\n", num1, num2, num1 - num2);
    }
}